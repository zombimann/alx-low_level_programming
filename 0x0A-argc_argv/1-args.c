#include <stdio.h>

/**
 * main - prints its name followed by a newline
 * @argc: the number of arguments
 * @argv: the character array of pointers to the arguments
 *
 * Return: 0 for successful execution
 */

int main(int argc, char *argv[])
{
	argv = argv;
	printf("%d\n", argc - 1);
	return (0);
}

